// Variable Hoisting with var                                    #
//                                                               #
// console.log(num) // undefined                                 #
// var num = 5;                                                  #
// console.log(num) // 5                                         #
// JS run code in two phases                                     #
//      ↣ Memory Creation Phase (Hoisting Phase)                #
//      ↣ Execution Phase                                       #
//                                                               #
//  During Memory creation phase----                             #
//  JS first scan code >> {var num} Hoist                        #
//      ..>> space created in memory with name {num}             #
//      ..>> vlaue = undefined                                   #
//                                                               #
// During Execution Phase                                        #
//  ..>> {console.log(num)} >> num exists, value = undefined     #
//  ..>> {num = 5} >> now value assigned                         #
//  ..>> {consloe.log(num)} >> 5                                 #
